#include "libft.h"

int	ft_atoi(const char *str)
{
	int	a;
	int	b;
	int	neg;

	a = 0;
	b = 0;
	neg = 1;
	while (str[a] == ' ' || (str[a] >= 9 && str[a] <= 13))
		a++;
	if (str[a] == '+' || str[a] == '-')
	{
		if (str[a] == '-')
			neg = -1;
		a++;
	}
	while (str[a] >= '0' && str[a] <= '9')
		b = ((b * 10) + (str[a++] - '0'));
	return (b * neg);
}
