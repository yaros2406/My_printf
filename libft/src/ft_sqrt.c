#include "libft.h"

int		ft_sqrt(int nb)
{
	int i;
	int res;

	i = 1;
	res = 0;
	if ((nb > -2147483648) && (nb < 2147483647))
	{
		while (i <= nb)
		{
			res = i * i;
			if (res == nb)
				return (i);
			else
				i = i + 1;
		}
	}
	return (0);
}
